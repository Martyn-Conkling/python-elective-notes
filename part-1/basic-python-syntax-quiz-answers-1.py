# Numbers:

# Integers (int): Whole numbers, positive or negative, without decimals. Example: 5, -3.
# Floating-point numbers (float): Numbers with a decimal point or in exponential form. Example: 3.14, -0.001, 2e2.
# Complex numbers (complex): Numbers with a real and imaginary part. Example: 3+5j.
# Boolean (bool):

# Represents truth value. It can be either True or False.
# Strings (str):

# A sequence of characters used to store text. Example: "Hello, World!".
# List (list):

# An ordered collection of items that can be of different data types. Lists are mutable, meaning their elements can be changed. Example: [1, 2.5, 'python', True].
# Tuple (tuple):

# Similar to lists, but immutable. Once created, its items cannot be changed. Example: (1, 2.5, 'python', True).
# Dictionary (dict):

# An unordered collection of key-value pairs. Dictionaries are mutable, and keys must be unique. Example: {'name': 'John', 'age': 30}.
# Set (set):

# An unordered collection of unique items. Sets are mutable and do not allow duplicate elements. Example: {1, 2, 3, 4}.
# Frozenset (frozenset):

# Similar to a set but immutable. Once created, it cannot be changed. Example: frozenset([1, 2, 3, 4]).

